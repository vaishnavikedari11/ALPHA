public class KBitManipulation1 {
    public static void main(String[] args) {
        //binary AND 
        System.out.println(5 & 6);
        //binary OR
        System.out.println(5 | 6);
        //binary XOR
        System.out.println(5^6);
        //binary ones complement
        System.out.println(~5);
        System.out.println(~0);
        //binary left shift
        System.out.println(5<<2);
        //binary right shift
        System.out.println(6>>1);
    }
}
